# estimate the price cost for family passengers:
# given the adult ticket price of $125
# and the child ticket price is $70
# and the number of adults is 2
# and the number of children is 3

adult_ticket_price = 125
child_ticket_price = 70
number_of_adults = 2
number_of_children = 3

total_cost = (adult_ticket_price * number_of_adults) + (child_ticket_price * number_of_children)

print("cost for family passengers")
print("---------------------------")
print("total cost:", total_cost)
print("---------------------------")
